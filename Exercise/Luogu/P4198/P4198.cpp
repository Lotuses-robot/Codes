const int maxn = 1e5 + 10;
struct Tree {
    struct Node {
        int l, r, lc, rc;
        int max_, ans;
    } nd[maxn << 2];
    int cnt;

    int bt(int l, int r) {
        int id = ++cnt;
        Node &d = nd[id];
        d.l = l; d.r = r;
        d.max_ = d.ans = 0;
        if (l != r) {
            int mid = (l + r) >> 1;
            d.lc = bt(l, mid);
            d.rc = bt(mid + 1, r);
        }
        return id;
    }
} tr;

