tsz main() {
    #ifdef LOCAL
        freopen(".in", "r", stdin);
        freopen(".out", "w", stdout);
    #endif
    
    int T;
    read(T);
    while (T--) {
        std::vector<int> v;
        int a, b, c;
        read(a, b, c);
        v.push_back(a);
        v.push_back(b);
        v.push_back(c);
        std::sort(v.begin(), v.end());
        for (int i = 1; i <= 3; i++) {
            int mx = v.back(), mn = v.front();
            if (mx == mn) break;
            v.pop_back();
            v.push_back(mn);
            v.push_back(mx - mn);
            std::sort(v.begin(), v.end());
        }
        if (v.back() != v.front()) puts("No");
        else puts("Yes");
    }
    return 0;
}