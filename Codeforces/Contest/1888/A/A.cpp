const int maxn = 1e5 + 10;
char ch[maxn];
int cnt[maxn];

tsz main() {
    #ifdef LOCAL
        freopen(".in", "r", stdin);
        freopen(".out", "w", stdout);
    #endif
    
    int T;
    read(T);
    while (T--) {
        memset(cnt, 0, sizeof(int) * 256);
        int n, k;
        read(n, k);
        scanf("%s", ch + 1);
        for (int i = 1; i <= n; i++) {
            cnt[ch[i]]++;
        }
        int ct = 0;
        for (int i = 'a'; i <= 'z'; i++) {
            if (cnt[i] % 2) {
                ct++;
            }
        }
        ct -= k;
        puts(ct <= 1 ? "YES" : "NO");
    }
    return 0;
}