#define int long long

const int maxn = 1e6 + 10, mod = 1e9 + 7;
int s[maxn];

tsz main() {
    #ifdef LOCAL
        freopen(".in", "r", stdin);
        freopen(".out", "w", stdout);
    #endif
    
    int T;
    read(T);
    while (T--) {
        int a, b, n;
        read(a, b, n);
        s[0] = 2;
        s[1] = a;
        for (int i = 2; i <= n; i++) {
            s[i] = s[i - 1] * a % mod - s[i - 2] * b % mod;
            s[i] = (s[i] % mod + mod) % mod;
        }
        writeln(s[n]);
    }
    return 0;
}